#include "minishell.h"

int is_quote(char c)
{
    if (c == '\'' || c == '"')
        return ((int) c);
    return (0);
}

int	is_space(char c)
{
	if (c == 32 || (c < 14 && c > 8) || c == '\0')
		return (1);
	return (0);
}
